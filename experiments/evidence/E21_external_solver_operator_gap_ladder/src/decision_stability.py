LAMBDA_GRID = [1e-4, 1e-3, 1e-2, 1e-1, 1.0, 10.0]


@dataclass
class RidgeEvidenceResult:
    operator: str = ""
    scorer_name: str = "ridge_evidence"
    best_lambda: float = 0.0
    same_operator_accuracy: float = 0.0
    cross_operator_accuracy: Dict[str, float] = field(default_factory=dict)
    instability_ratio: Dict[str, float] = field(default_factory=dict)
    confusion_matrix_same: List[List[int]] = field(default_factory=list)
    confusion_matrix_cross: Dict[str, List[List[int]]] = field(default_factory=dict)
    false_via_rate: float = 0.0
    return_path_confusion_rate: float = 0.0
    n_samples_per_class: int = 0


@dataclass
class MarginRefusalResult:
    operator: str = ""
    margin_threshold: float = 0.0
    accepted_rate_same: float = 0.0
    accepted_accuracy_same: float = 0.0
    accepted_rate_cross: Dict[str, float] = field(default_factory=dict)
    accepted_accuracy_cross: Dict[str, float] = field(default_factory=dict)
    wrong_accept_rate_cross: Dict[str, float] = field(default_factory=dict)
    refusal_rate_cross: Dict[str, float] = field(default_factory=dict)


class RidgeEvidenceScorer:
    """Per-hypothesis ridge evidence scorer with lambda grid.

    For each hypothesis h, builds a template T_h (class-mean field) and scores:

        score_h = min_alpha ||y - alpha * T_h||^2 + lambda * alpha^2

    Closed-form: alpha* = (T_h^T y) / (||T_h||^2 + lambda)
                 score_h = ||y - alpha* T_h||^2 + lambda * alpha*^2

    Lambda selected by best same-operator accuracy on a calibration split.
    """

    def __init__(self, lambda_grid: List[float] = None):
        self.lambda_grid = lambda_grid if lambda_grid is not None else LAMBDA_GRID
        self.best_lambda: float = 1e-3
        self.templates_: np.ndarray = None
        self.template_norms_sq_: np.ndarray = None
        self.classes_: List[int] = []
        self.n_classes_: int = 0
        self.calib_acc_: Dict[float, float] = {}

    def fit(self, X: np.ndarray, y: np.ndarray) -> "RidgeEvidenceScorer":
        self.classes_ = sorted(set(int(c) for c in y))
        self.n_classes_ = len(self.classes_)
        n = X.shape[0]

        rng = np.random.default_rng(42)
        idx = rng.permutation(n)
        n_cal = max(1, n // 5)
        idx_train = idx[:n - n_cal]
        idx_cal = idx[n - n_cal:]

        self.templates_ = np.zeros((self.n_classes_, X.shape[1]))
        for idx_c, c in enumerate(self.classes_):
            mask = y[idx_train] == c
            if np.sum(mask) > 0:
                self.templates_[idx_c] = X[idx_train][mask].mean(axis=0)
        self.template_norms_sq_ = np.sum(self.templates_ ** 2, axis=1)

        X_cal = X[idx_cal]
        y_cal = y[idx_cal]
        best_acc = 0.0
        self.best_lambda = self.lambda_grid[0]
        for lam in self.lambda_grid:
            scores = self._compute_scores(X_cal, lam)
            pred = np.array([self.classes_[i] for i in np.argmin(scores, axis=1)])
            acc = float(np.mean(pred == y_cal))
            self.calib_acc_[lam] = acc
            if acc > best_acc:
                best_acc = acc
                self.best_lambda = lam
        return self

    def _compute_scores(self, X: np.ndarray, lam: float) -> np.ndarray:
        n_samples = X.shape[0]
        scores = np.zeros((n_samples, self.n_classes_))
        for idx in range(self.n_classes_):
            T = self.templates_[idx]
            tn_sq = self.template_norms_sq_[idx]
            eps = 1e-30
            denom = tn_sq + lam + eps
            alpha = (X @ T) / denom
            residuals = np.sum((X - alpha[:, None] * T[None, :]) ** 2, axis=1)
            scores[:, idx] = residuals + lam * alpha ** 2
        return scores

    def predict(self, X: np.ndarray, lam: float = None) -> np.ndarray:
        if lam is None:
            lam = self.best_lambda
        scores = self._compute_scores(X, lam)
        return np.array([self.classes_[i] for i in np.argmin(scores, axis=1)])

    def decision_margin(self, X: np.ndarray, lam: float = None) -> np.ndarray:
        if lam is None:
            lam = self.best_lambda
        scores = self._compute_scores(X, lam)
        sorted_scores = np.sort(scores, axis=1)
        return sorted_scores[:, 1] - sorted_scores[:, 0]

    def predict_cross_operator(
        self, X: np.ndarray, op_templates: Dict[int, np.ndarray],
        lam: float = None,
    ) -> np.ndarray:
        if lam is None:
            lam = self.best_lambda
        classes = sorted(op_templates.keys())
        n_classes = len(classes)
        templates = np.stack([op_templates[c] for c in classes], axis=0)
        tn_sq = np.sum(templates ** 2, axis=1)
        n_samples = X.shape[0]
        scores = np.zeros((n_samples, n_classes))
        for idx in range(n_classes):
            T = templates[idx]
            denom = tn_sq[idx] + lam + 1e-30
            alpha = (X @ T) / denom
            residuals = np.sum((X - alpha[:, None] * T[None, :]) ** 2, axis=1)
            scores[:, idx] = residuals + lam * alpha ** 2
        pred_indices = np.argmin(scores, axis=1)
        return np.array([classes[i] for i in pred_indices])

    def decision_margin_cross_operator(
        self, X: np.ndarray, op_templates: Dict[int, np.ndarray],
        lam: float = None,
    ) -> np.ndarray:
        if lam is None:
            lam = self.best_lambda
        classes = sorted(op_templates.keys())
        n_classes = len(classes)
        templates = np.stack([op_templates[c] for c in classes], axis=0)
        tn_sq = np.sum(templates ** 2, axis=1)
        n_samples = X.shape[0]
        scores = np.zeros((n_samples, n_classes))
        for idx in range(n_classes):
            T = templates[idx]
            denom = tn_sq[idx] + lam + 1e-30
            alpha = (X @ T) / denom
            residuals = np.sum((X - alpha[:, None] * T[None, :]) ** 2, axis=1)
            scores[:, idx] = residuals + lam * alpha ** 2
        sorted_scores = np.sort(scores, axis=1)
        return sorted_scores[:, 1] - sorted_scores[:, 0]


def run_ridge_evidence_stress(
    points: np.ndarray,
    geom_segments: List,
    op_names: List[str],
    n_per_class: int = 25,
    current_max_mA: float = 10.0,
    noise_std_uT: float = 0.1,
    rng: np.random.Generator = None,
) -> Tuple[Dict[str, RidgeEvidenceResult], Dict[str, MarginRefusalResult]]:
    if rng is None:
        rng = np.random.default_rng(0)

    n_components = min(len(geom_segments), 4)
    if n_components < 2:
        return {}, {}

    currents_dict = {}
    labels_dict = {}
    for op_name in op_names:
        currents, labels = generate_mechanism_currents(
            n_per_class, n_components, current_max_mA, rng
        )
        currents_dict[op_name] = currents
        labels_dict[op_name] = labels

    op_fields = {}
    for op_name in op_names:
        op_fields[op_name] = compute_mechanism_fields(
            currents_dict[op_name], points, geom_segments, op_name,
            noise_std_uT=noise_std_uT, rng=rng,
        )

    all_margins = []
    for train_op in op_names:
        if train_op not in op_fields:
            continue
        X = op_fields[train_op].reshape(len(currents_dict[train_op]), -1)
        y = labels_dict[train_op]
        scorer = RidgeEvidenceScorer()
        scorer.fit(X, y)
        margins = scorer.decision_margin(X)
        all_margins.extend(margins.tolist())
    margin_threshold = float(np.median(all_margins)) if all_margins else 0.0

    ridge_results = {}
    margin_results = {}

    for train_op in op_names:
        if train_op not in op_fields:
            continue

        X_train = op_fields[train_op].reshape(len(currents_dict[train_op]), -1)
        y_train = labels_dict[train_op]

        scorer = RidgeEvidenceScorer()
        scorer.fit(X_train, y_train)

        pred_same = scorer.predict(X_train)
        same_acc = float(np.mean(pred_same == y_train))
        cm_same = _confusion_matrix(y_train, pred_same, scorer.n_classes_)
        margins_same = scorer.decision_margin(X_train)

        accept_mask_same = margins_same >= margin_threshold
        accepted_rate_same = float(np.mean(accept_mask_same))
        accepted_acc_same = float(np.mean(pred_same[accept_mask_same] == y_train[accept_mask_same])) if np.sum(accept_mask_same) > 0 else 0.0

        op_templates = {}
        for idx_c, c in enumerate(scorer.classes_):
            mask = y_train == c
            if np.sum(mask) > 0:
                op_templates[int(c)] = X_train[mask].mean(axis=0)

        cross_accs = {}
        ratios = {}
        cm_cross = {}
        accepted_rate_cross = {}
        accepted_acc_cross = {}
        wrong_accept_cross = {}
        refusal_rate_cross = {}

        for test_op in op_names:
            if test_op not in op_fields:
                continue
            X_test = op_fields[test_op].reshape(len(currents_dict[test_op]), -1)
            y_test = labels_dict[test_op]

            if test_op == train_op:
                pred = scorer.predict(X_test)
                margins = scorer.decision_margin(X_test)
            else:
                pred = scorer.predict_cross_operator(X_test, op_templates)
                margins = scorer.decision_margin_cross_operator(X_test, op_templates)

            acc = float(np.mean(pred == y_test))
            cross_accs[test_op] = acc
            if test_op != train_op:
                ratios[f"{train_op}_vs_{test_op}"] = float(same_acc / (acc + 1e-30))
                cm_cross[test_op] = _confusion_matrix(y_test, pred, scorer.n_classes_).tolist()

            accept_mask = margins >= margin_threshold
            accepted_rate_cross[test_op] = float(np.mean(accept_mask))
            if np.sum(accept_mask) > 0:
                accepted_acc_cross[test_op] = float(np.mean(pred[accept_mask] == y_test[accept_mask]))
                wrong_accept_cross[test_op] = float(np.mean(pred[accept_mask] != y_test[accept_mask]))
            else:
                accepted_acc_cross[test_op] = 0.0
                wrong_accept_cross[test_op] = 0.0
            refusal_rate_cross[test_op] = 1.0 - float(np.mean(accept_mask))

        h0_mask = y_train == 0
        false_via = float(np.mean(pred_same[h0_mask] == 1)) if np.sum(h0_mask) > 0 else 0.0
        h3_mask = y_train == 3
        rp_conf = float(np.mean(pred_same[h3_mask] == 1)) if np.sum(h3_mask) > 0 else 0.0

        ridge_results[train_op] = RidgeEvidenceResult(
            operator=train_op,
            best_lambda=scorer.best_lambda,
            same_operator_accuracy=same_acc,
            cross_operator_accuracy=cross_accs,
            instability_ratio=ratios,
            confusion_matrix_same=cm_same.tolist() if hasattr(cm_same, 'tolist') else [[int(v) for v in row] for row in cm_same],
            confusion_matrix_cross=cm_cross,
            false_via_rate=false_via,
            return_path_confusion_rate=rp_conf,
            n_samples_per_class=n_per_class,
        )

        margin_results[train_op] = MarginRefusalResult(
            operator=train_op,
            margin_threshold=margin_threshold,
            accepted_rate_same=accepted_rate_same,
            accepted_accuracy_same=accepted_acc_same,
            accepted_rate_cross=accepted_rate_cross,
            accepted_accuracy_cross=accepted_acc_cross,
            wrong_accept_rate_cross=wrong_accept_cross,
            refusal_rate_cross=refusal_rate_cross,
        )

    return ridge_results, margin_results


# --- Round 5: Multi-basis evidence scorer and margin-shift operator-gap certificate ---

@dataclass
class MultiBasisResult:
    operator: str = ""
    scorer_name: str = "multibasis_evidence"
    k_components: int = 0
    energy_retained: float = 0.0
    same_operator_accuracy: float = 0.0
    cross_operator_accuracy: Dict[str, float] = field(default_factory=dict)
    cross_operator_drop: float = 0.0
    confusion_matrix_same: List[List[int]] = field(default_factory=list)
    confusion_matrix_cross: Dict[str, List[List[int]]] = field(default_factory=dict)
    false_via_rate: float = 0.0
    return_path_confusion_rate: float = 0.0
    n_samples_per_class: int = 0


@dataclass
class MarginShiftCertificate:
    interclass_delta_min: float = 0.0
    interclass_delta_by_pair: Dict[str, float] = field(default_factory=dict)
    operator_shift_radius_by_hypothesis: Dict[str, float] = field(default_factory=dict)
    operator_shift_radius_max: float = 0.0
    gap_to_margin_ratio: float = 0.0
    stable_classification_possible_by_margin: bool = False
    certificate_summary: str = ""


class MultiBasisEvidenceScorer:
    """Per-hypothesis low-rank subspace evidence scorer.

    For each mechanism class h:
      - Builds orthonormal basis Q_h via PCA (energy threshold)
      - Scores: d_h(y)^2 = ||y - Q_h Q_h^T y||^2 (projection residual)
      - Predicts: argmin_h d_h(y)^2
    """

    def __init__(self, k_components: int = 3, energy_threshold: float = 0.90):
        self.k_components = k_components
        self.energy_threshold = energy_threshold
        self.bases_: Dict[int, np.ndarray] = {}  # per-class orthonormal basis
        self.energies_: Dict[int, float] = {}
        self.classes_: List[int] = []
        self.n_classes_: int = 0
        self.effective_k_: int = 0

    def fit(self, X: np.ndarray, y: np.ndarray) -> "MultiBasisEvidenceScorer":
        self.classes_ = sorted(set(int(c) for c in y))
        self.n_classes_ = len(self.classes_)
        min_k = self.k_components
        for c in self.classes_:
            mask = y == c
            X_c = X[mask]
            if X_c.shape[0] < 2:
                self.bases_[int(c)] = np.zeros((X.shape[1], 0))
                self.energies_[int(c)] = 0.0
                continue
            X_centered = X_c - X_c.mean(axis=0)
            U, s, Vt = np.linalg.svd(X_centered, full_matrices=False)
            total_var = np.sum(s ** 2) + 1e-30
            cum_energy = np.cumsum(s ** 2) / total_var
            k_by_energy = int(np.searchsorted(cum_energy, self.energy_threshold) + 1)
            k_use = max(1, min(self.k_components, k_by_energy, len(s)))
            min_k = min(min_k, k_use)
            # Vt has shape (min(n_samples, n_features), n_features)
            # Vt.T[:, :k] gives (n_features, k) orthonormal basis
            self.bases_[int(c)] = Vt.T[:, :k_use]
            self.energies_[int(c)] = float(cum_energy[min(k_use - 1, len(cum_energy) - 1)])
        # Use equal k for all classes (minimum across classes)
        self.effective_k_ = min_k
        for c in self.classes_:
            if self.bases_[int(c)].shape[1] > min_k:
                self.bases_[int(c)] = self.bases_[int(c)][:, :min_k]
        return self

    def predict(self, X: np.ndarray) -> np.ndarray:
        n_samples = X.shape[0]
        scores = np.full((n_samples, self.n_classes_), np.inf)
        for idx, c in enumerate(self.classes_):
            Q = self.bases_.get(int(c))
            if Q is None or Q.shape[1] == 0:
                scores[:, idx] = np.sum(X ** 2, axis=1)
            else:
                proj = X @ (Q @ Q.T)  # X projected onto span(Q)
                residuals = np.sum((X - proj) ** 2, axis=1)
                scores[:, idx] = residuals
        pred_indices = np.argmin(scores, axis=1)
        return np.array([self.classes_[i] for i in pred_indices])

    def decision_margin(self, X: np.ndarray) -> np.ndarray:
        n_samples = X.shape[0]
        scores = np.full((n_samples, self.n_classes_), np.inf)
        for idx, c in enumerate(self.classes_):
            Q = self.bases_.get(int(c))
            if Q is None or Q.shape[1] == 0:
                scores[:, idx] = np.sum(X ** 2, axis=1)
            else:
                proj = X @ (Q @ Q.T)
                scores[:, idx] = np.sum((X - proj) ** 2, axis=1)
        sorted_scores = np.sort(scores, axis=1)
        return sorted_scores[:, 1] - sorted_scores[:, 0]

    def predict_cross_operator(self, X: np.ndarray) -> np.ndarray:
        """Predict using the same bases — operator shift is in the data, not the model."""
        return self.predict(X)


def compute_margin_shift_certificate(
    points: np.ndarray,
    geom_segments: List,
    op_names: List[str],
    n_per_class: int = 25,
    current_max_mA: float = 10.0,
    noise_std_uT: float = 0.0,
    rng: np.random.Generator = None,
) -> MarginShiftCertificate:
    """Compute the operator-gap margin-shift certificate.

    interclass_delta_hg = min distance between observation vectors from class h and g.
    operator_shift_radius s_h = max ||B_{opB}(i) - B_{opA}(i)|| over class h currents.
    """
    if rng is None:
        rng = np.random.default_rng(0)

    n_components = min(len(geom_segments), 4)
    if n_components < 2:
        return MarginShiftCertificate()

    # Use a single base operator (centerline) and all others as perturbed
    base_op = "centerline_biot_savart" if "centerline_biot_savart" in op_names else op_names[0]
    perturb_ops = [op for op in op_names if op != base_op]

    if not perturb_ops:
        return MarginShiftCertificate()

    # Generate mechanism currents shared across operators
    currents, labels = generate_mechanism_currents(
        n_per_class, n_components, current_max_mA, rng
    )

    # Compute fields for base operator
    base_fields = compute_mechanism_fields(
        currents, points, geom_segments, base_op,
        noise_std_uT=noise_std_uT, rng=rng,
    )

    # Compute inter-class deltas from base operator fields
    n_features = base_fields.shape[1] * base_fields.shape[2]
    base_flat = base_fields.reshape(len(currents), -1)
    class_indices = {}
    for c in range(4):
        class_indices[c] = np.where(labels == c)[0]

    interclass_delta_by_pair = {}
    all_deltas = []
    classes_sorted = sorted(class_indices.keys())
    for i in range(len(classes_sorted)):
        for j in range(i + 1, len(classes_sorted)):
            ci, cj = classes_sorted[i], classes_sorted[j]
            idx_i = class_indices[ci]
            idx_j = class_indices[cj]
            if len(idx_i) == 0 or len(idx_j) == 0:
                continue
            Fi = base_flat[idx_i]
            Fj = base_flat[idx_j]
            # Minimum pairwise distance between sets
            min_dist = float("inf")
            for a in range(min(len(idx_i), 20)):
                diffs = np.sum((Fi[a:a+1, :] - Fj) ** 2, axis=1)
                min_dist = min(min_dist, float(np.min(diffs)))
            pair_key = f"H{ci}_vs_H{cj}"
            interclass_delta_by_pair[pair_key] = np.sqrt(min_dist)
            all_deltas.append(np.sqrt(min_dist))

    interclass_delta_min = float(np.min(all_deltas)) if all_deltas else 0.0

    # Compute operator shift radius per hypothesis
    shift_by_hypothesis = {}
    all_shifts = []
    for perturb_op in perturb_ops:
        pfields = compute_mechanism_fields(
            currents, points, geom_segments, perturb_op,
            noise_std_uT=noise_std_uT, rng=rng,
        )
        pflat = pfields.reshape(len(currents), -1)
        shifts = np.sqrt(np.sum((pflat - base_flat) ** 2, axis=1))
        for c in range(4):
            mask = labels == c
            if np.sum(mask) > 0:
                key = f"{perturb_op}_H{c}"
                shift_by_hypothesis[key] = float(np.max(shifts[mask]))
                all_shifts.append(float(np.max(shifts[mask])))

    shift_max = float(np.max(all_shifts)) if all_shifts else 0.0
    eps = 1e-30
    gap_to_margin_ratio = shift_max / max(interclass_delta_min, eps)
    stable = bool(shift_max < 0.5 * interclass_delta_min)

    cert_summary = (
        f"min_interclass_delta={interclass_delta_min:.3e}, "
        f"max_operator_shift={shift_max:.3e}, "
        f"ratio={gap_to_margin_ratio:.3f}, "
        f"stable_possible={stable}. "
    )
    if not stable:
        cert_summary += (
            f"OPERATOR GAP EXCEEDS MECHANISM MARGIN: "
            f"shift {shift_max:.3e} >= 0.5 * delta {0.5*interclass_delta_min:.3e}. "
            f"No classifier can be stable under these operator perturbations."
        )
    else:
        cert_summary += (
            f"Operator shift is within margin: stable classification is theoretically possible."
        )

    return MarginShiftCertificate(
        interclass_delta_min=interclass_delta_min,
        interclass_delta_by_pair=interclass_delta_by_pair,
        operator_shift_radius_by_hypothesis=shift_by_hypothesis,
        operator_shift_radius_max=shift_max,
        gap_to_margin_ratio=gap_to_margin_ratio,
        stable_classification_possible_by_margin=stable,
        certificate_summary=cert_summary,
    )


def run_multibasis_stress(
    points: np.ndarray,
    geom_segments: List,
    op_names: List[str],
    n_per_class: int = 25,
    current_max_mA: float = 10.0,
    noise_std_uT: float = 0.1,
    k_components: int = 3,
    rng: np.random.Generator = None,
) -> Dict[str, MultiBasisResult]:
    """Run multi-basis evidence scorer across operator shifts."""
    if rng is None:
        rng = np.random.default_rng(0)

    n_components = min(len(geom_segments), 4)
    if n_components < 2:
        return {}

    currents_dict = {}
    labels_dict = {}
    for op_name in op_names:
        currents, labels = generate_mechanism_currents(
            n_per_class, n_components, current_max_mA, rng
        )
        currents_dict[op_name] = currents
        labels_dict[op_name] = labels

    op_fields = {}
    for op_name in op_names:
        op_fields[op_name] = compute_mechanism_fields(
            currents_dict[op_name], points, geom_segments, op_name,
            noise_std_uT=noise_std_uT, rng=rng,
        )

    results = {}

    for train_op in op_names:
        if train_op not in op_fields:
            continue

        X_train = op_fields[train_op].reshape(len(currents_dict[train_op]), -1)
        y_train = labels_dict[train_op]

        scorer = MultiBasisEvidenceScorer(k_components=k_components)
        scorer.fit(X_train, y_train)

        pred_same = scorer.predict(X_train)
        same_acc = float(np.mean(pred_same == y_train))
        cm_same = _confusion_matrix(y_train, pred_same, scorer.n_classes_)

        cross_accs = {}
        cm_cross_out = {}
        for test_op in op_names:
            if test_op not in op_fields:
                continue
            X_test = op_fields[test_op].reshape(len(currents_dict[test_op]), -1)
            y_test = labels_dict[test_op]
            pred = scorer.predict_cross_operator(X_test)
            acc = float(np.mean(pred == y_test))
            cross_accs[test_op] = acc
            if test_op != train_op:
                cm_cross_out[test_op] = _confusion_matrix(y_test, pred, scorer.n_classes_).tolist()

        cross_vals = [v for k, v in cross_accs.items() if k != train_op]
        cross_drop = same_acc - min(cross_vals) if cross_vals else 0.0

        h0_mask = y_train == 0
        false_via = float(np.mean(pred_same[h0_mask] == 1)) if np.sum(h0_mask) > 0 else 0.0
        h3_mask = y_train == 3
        rp_conf = float(np.mean(pred_same[h3_mask] == 1)) if np.sum(h3_mask) > 0 else 0.0

        results[train_op] = MultiBasisResult(
            operator=train_op,
            k_components=scorer.effective_k_,
            energy_retained=float(np.mean(list(scorer.energies_.values()))) if scorer.energies_ else 0.0,
            same_operator_accuracy=same_acc,
            cross_operator_accuracy=cross_accs,
            cross_operator_drop=cross_drop,
            confusion_matrix_same=cm_same.tolist() if hasattr(cm_same, 'tolist') else [[int(v) for v in row] for row in cm_same],
            confusion_matrix_cross=cm_cross_out,
            false_via_rate=false_via,
            return_path_confusion_rate=rp_conf,
            n_samples_per_class=n_per_class,
        )

    return results
