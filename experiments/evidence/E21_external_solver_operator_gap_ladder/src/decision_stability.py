"""Decision stability: how inverse/hypothesis decisions change when the operator changes.

Key quantity: DecisionGap(i,j) = Pr[decision_i(y_j) != decision_j(y_j)]

Where decision_i is an inverse or hypothesis scorer calibrated under operator A_i,
and y_j is generated by operator A_j.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Dict, List, Optional, Tuple

import numpy as np


@dataclass
class InverseResult:
    method: str
    operator: str
    predictions: np.ndarray
    rel_l2: float
    per_component_rel_l2: List[float]


@dataclass
class DecisionSwap:
    pair: str
    same_operator_error: float
    cross_operator_error: float
    instability_ratio: float
    n_samples: int


def build_design_matrix(field_map: np.ndarray) -> np.ndarray:
    """Flatten field map (n, n, 3) into (n*n, 3) design matrix."""
    n = field_map.shape[0]
    return field_map.reshape(n * n, 3)


def fit_ridge_decoder(
    A: np.ndarray,
    targets: np.ndarray,
    alpha: float = 1e-3,
) -> np.ndarray:
    """Fit ridge regression: targets ≈ A @ W, returns W."""
    gram = A.T @ A
    reg = gram + alpha * np.eye(gram.shape[0])
    return np.linalg.solve(reg, A.T @ targets)


def predict_currents(
    B_flat: np.ndarray,
    decoder: np.ndarray,
) -> np.ndarray:
    """Predict source currents from field observations."""
    return B_flat @ decoder


def generate_training_data(
    operator_fn,
    points: np.ndarray,
    geom_segments: List,
    n_samples: int,
    n_components: int,
    current_max_mA: float,
    noise_std_uT: float,
    rng: np.random.Generator,
) -> Tuple[np.ndarray, np.ndarray]:
    """Generate (X, Y) pairs: fields → currents."""
    max_a = current_max_mA * 1e-3
    currents = rng.uniform(-max_a, max_a, size=(n_samples, n_components))
    fields = np.zeros((n_samples, points.shape[0], 3))
    for i in range(n_samples):
        B_total = np.zeros((points.shape[0], 3))
        for k, (p0, p1, _) in enumerate(geom_segments):
            if k < n_components:
                from operators_centerline import segment_field
                B_total += segment_field(points, p0, p1, currents[i, k])
        if noise_std_uT > 0:
            B_total += rng.normal(scale=noise_std_uT * 1e-6, size=B_total.shape)
        fields[i] = B_total
    return fields, currents


def run_inverse(
    train_fields: np.ndarray,
    train_currents: np.ndarray,
    test_fields: np.ndarray,
    test_currents: np.ndarray,
    method: str = "ridge",
    alpha: float = 1e-3,
) -> InverseResult:
    """Run a simple inverse (ridge) and return predictions."""
    n_test = test_fields.shape[0]
    n_components = train_currents.shape[1]

    A_train = train_fields.reshape(train_fields.shape[0], -1)
    A_test = test_fields.reshape(n_test, -1)

    if method == "ridge":
        gram = A_train.T @ A_train + alpha * np.eye(A_train.shape[1])
        W = np.linalg.solve(gram, A_train.T @ train_currents)
        pred = A_test @ W

    rel_l2 = float(np.linalg.norm(pred - test_currents) / (np.linalg.norm(test_currents) + 1e-30))
    per_comp = [
        float(np.linalg.norm(pred[:, c] - test_currents[:, c]) / (np.linalg.norm(test_currents[:, c]) + 1e-30))
        for c in range(n_components)
    ]

    return InverseResult(
        method=method,
        operator="ridge",
        predictions=pred,
        rel_l2=rel_l2,
        per_component_rel_l2=per_comp,
    )


@dataclass
class DecisionStabilityResult:
    swaps: List[DecisionSwap] = field(default_factory=list)
    same_operator_errors: Dict[str, float] = field(default_factory=dict)
    cross_operator_errors: Dict[str, float] = field(default_factory=dict)
    instability_summary: str = ""


def compute_decision_stability(
    operator_names: List[str],
    field_maps_train: Dict[str, np.ndarray],
    field_maps_test: Dict[str, np.ndarray],
    train_currents: Dict[str, np.ndarray],
    test_currents: Dict[str, np.ndarray],
    alpha: float = 1e-3,
) -> DecisionStabilityResult:
    """Compute decision instability: train decoder on op_i, test on op_j."""
    result = DecisionStabilityResult()
    decoders = {}

    # Train decoders on each operator
    for op_name in operator_names:
        if op_name not in field_maps_train:
            continue
        X = field_maps_train[op_name].reshape(field_maps_train[op_name].shape[0], -1)
        Y = train_currents[op_name]
        W = fit_ridge_decoder(X, Y, alpha)
        decoders[op_name] = W

    # Evaluate same-operator and cross-operator
    for train_op in operator_names:
        if train_op not in decoders:
            continue
        W = decoders[train_op]
        for test_op in operator_names:
            if test_op not in field_maps_test:
                continue
            X = field_maps_test[test_op].reshape(field_maps_test[test_op].shape[0], -1)
            Y = test_currents[test_op]
            pred = X @ W
            err = float(np.linalg.norm(pred - Y) / (np.linalg.norm(Y) + 1e-30))
            key = f"decoder_{train_op}_on_{test_op}"
            if train_op == test_op:
                result.same_operator_errors[train_op] = err
            else:
                result.cross_operator_errors[key] = err

    # Compute instability ratios
    for train_op in operator_names:
        if train_op not in result.same_operator_errors:
            continue
        same_err = result.same_operator_errors[train_op]
        for test_op in operator_names:
            if test_op == train_op:
                continue
            key = f"decoder_{train_op}_on_{test_op}"
            if key not in result.cross_operator_errors:
                continue
            cross_err = result.cross_operator_errors[key]
            ratio = float(cross_err / (same_err + 1e-30))
            result.swaps.append(DecisionSwap(
                pair=key,
                same_operator_error=same_err,
                cross_operator_error=cross_err,
                instability_ratio=ratio,
                n_samples=field_maps_test[test_op].shape[0],
            ))

    # Summary
    if result.swaps:
        ratios = [s.instability_ratio for s in result.swaps]
        result.instability_summary = (
            f"Cross/same error ratio: min={min(ratios):.1f}x, "
            f"median={float(np.median(ratios)):.1f}x, "
            f"max={max(ratios):.1f}x across {len(result.swaps)} operator swaps"
        )

    return result


# --- Round 2: Mechanism-level decision instability ---

@dataclass
class MechanismDecisionResult:
    """Decision instability at the mechanism/hypothesis level (H0/H1/H2/H3)."""
    operator: str = ""
    same_operator_accuracy: float = 0.0
    cross_operator_accuracy: Dict[str, float] = field(default_factory=dict)
    cross_same_accuracy_ratio: Dict[str, float] = field(default_factory=dict)
    confusion_matrix: List[List[int]] = field(default_factory=list)
    false_via_rate: float = 0.0  # H1 predicted when truth is H0
    return_path_confusion_rate: float = 0.0  # H3 confused with H1
    n_samples_per_class: int = 0
    mechanism_names: List[str] = field(default_factory=lambda: ["H0_no_via", "H1_via", "H2_model_gap", "H3_return_path"])


def generate_mechanism_currents(
    n_per_class: int,
    n_components: int,
    current_max_mA: float,
    rng: np.random.Generator,
) -> Tuple[np.ndarray, np.ndarray]:
    """Generate current configurations for H0/H1/H2/H3 hypothesis classes.

    Returns (currents, labels) where currents shape is (4*n_per_class, n_components)
    and labels are 0=H0, 1=H1, 2=H2, 3=H3.
    """
    max_a = current_max_mA * 1e-3
    currents = []
    labels = []

    # H0_no_via: currents only on layer 1, no via
    for _ in range(n_per_class):
        c = rng.uniform(-max_a, max_a, size=n_components)
        if n_components >= 3:
            c[2] = 0.0  # zero via current
        currents.append(c)
        labels.append(0)

    # H1_via: currents on both layers with via
    for _ in range(n_per_class):
        c = rng.uniform(-max_a, max_a, size=n_components)
        if n_components >= 3:
            c[2] = rng.uniform(0.3 * max_a, max_a) * np.sign(rng.uniform(-1, 1))
        currents.append(c)
        labels.append(1)

    # H2_model_gap: currents on layer 1 with a "gap" component
    for _ in range(n_per_class):
        c = rng.uniform(-max_a, max_a, size=n_components)
        c[0] *= 0.3  # weaker layer-1 current
        if n_components >= 3:
            c[2] = rng.uniform(-0.2 * max_a, 0.2 * max_a)
        currents.append(c)
        labels.append(2)

    # H3_return_path: currents with explicit return (negative second component)
    for _ in range(n_per_class):
        c = rng.uniform(-max_a, max_a, size=n_components)
        if n_components >= 2:
            c[1] = -abs(c[1]) * 0.8  # return current opposite sign
        if n_components >= 3:
            c[2] = rng.uniform(0.2 * max_a, max_a) * np.sign(c[0])
        currents.append(c)
        labels.append(3)

    return np.array(currents), np.array(labels)


def compute_mechanism_fields(
    currents: np.ndarray,
    points: np.ndarray,
    geom_segments: List,
    op_name: str,
    noise_std_uT: float,
    rng: np.random.Generator,
    op_cfgs: List = None,
) -> np.ndarray:
    """Compute field maps for given currents using the specified operator."""
    from operators_centerline import segment_field
    from operators_finite_width import (
        finite_width_forward, missing_return_forward,
        deep_layer_shift_forward, registration_gap_forward,
    )

    n_samples = currents.shape[0]
    n_components = currents.shape[1]
    fields = np.zeros((n_samples, points.shape[0], 3))

    for i in range(n_samples):
        B_total = np.zeros((points.shape[0], 3))

        if op_name == "missing_return_surrogate":
            B_total, _ = missing_return_forward(
                points, geom_segments,
                width_um=20.0, n_filaments=5, depth_shift_um=3.0,
                noise_std_uT=noise_std_uT, rng=rng,
            )
            # Scale by currents
            for k, (p0, p1, _) in enumerate(geom_segments):
                if k < n_components and currents[i, k] != 1.0:
                    B_seg, _ = missing_return_forward(
                        points, [(p0, p1, "")], width_um=20.0, n_filaments=5,
                        depth_shift_um=3.0, noise_std_uT=0.0, rng=rng,
                    )
                    B_total += B_seg * (currents[i, k] - 1.0)

        elif op_name == "deep_layer_shift_surrogate":
            B_total, _ = deep_layer_shift_forward(
                points, geom_segments, depth_shift_um=15.0,
                noise_std_uT=noise_std_uT, rng=rng,
            )
            for k, (p0, p1, _) in enumerate(geom_segments):
                if k < n_components and currents[i, k] != 1.0:
                    B_seg, _ = deep_layer_shift_forward(
                        points, [(p0, p1, "")], depth_shift_um=15.0,
                        noise_std_uT=0.0, rng=rng,
                    )
                    B_total += B_seg * (currents[i, k] - 1.0)

        elif op_name == "registration_gap_surrogate":
            B_total, _ = registration_gap_forward(
                points, geom_segments, shift_x_um=10.0, shift_y_um=5.0,
                noise_std_uT=noise_std_uT, rng=rng,
            )
            for k, (p0, p1, _) in enumerate(geom_segments):
                if k < n_components and currents[i, k] != 1.0:
                    B_seg, _ = registration_gap_forward(
                        points, [(p0, p1, "")], shift_x_um=10.0, shift_y_um=5.0,
                        noise_std_uT=0.0, rng=rng,
                    )
                    B_total += B_seg * (currents[i, k] - 1.0)

        else:
            # Default: centerline segment_field
            for k, (p0, p1, _) in enumerate(geom_segments):
                if k < n_components:
                    B_total += segment_field(points, p0, p1, currents[i, k])

        if noise_std_uT > 0:
            B_total += rng.normal(scale=noise_std_uT * 1e-6, size=B_total.shape)
        fields[i] = B_total

    return fields


def run_mechanism_decision_stress(
    points: np.ndarray,
    geom_segments: List,
    op_names: List[str],
    n_per_class: int = 25,
    current_max_mA: float = 10.0,
    noise_std_uT: float = 0.1,
    rng: np.random.Generator = None,
) -> Dict[str, MechanismDecisionResult]:
    """Run mechanism-level decision instability: H0/H1/H2/H3 classification.

    For each operator pair (train on op_i, test on op_j), train a linear
    classifier and report mechanism accuracy, confusion, false-via rate.
    """
    if rng is None:
        rng = np.random.default_rng(0)

    n_components = min(len(geom_segments), 4)
    if n_components < 2:
        return {}

    # Generate mechanism-labeled currents
    currents, labels = generate_mechanism_currents(
        n_per_class, n_components, current_max_mA, rng
    )
    total_samples = len(currents)

    # Generate fields for each operator
    op_fields = {}
    for op_name in op_names:
        op_fields[op_name] = compute_mechanism_fields(
            currents, points, geom_segments, op_name,
            noise_std_uT=noise_std_uT, rng=rng,
        )

    results = {}

    # For each operator, train on its fields, test on all operators
    for train_op in op_names:
        if train_op not in op_fields:
            continue

        X_train = op_fields[train_op].reshape(total_samples, -1)
        y_train = labels

        # Train linear classifier (one-vs-rest logistic regression via ridge + argmax)
        n_classes = 4
        W = np.zeros((X_train.shape[1], n_classes))
        for c in range(n_classes):
            y_c = (y_train == c).astype(float)
            gram = X_train.T @ X_train + 1e-3 * np.eye(X_train.shape[1])
            W[:, c] = np.linalg.solve(gram, X_train.T @ y_c)

        same_acc = 0.0
        cross_accs = {}
        ratios = {}

        # Confusion matrix for same-operator
        logits = X_train @ W
        pred_train = np.argmax(logits, axis=1)
        same_acc = float(np.mean(pred_train == y_train))
        cm = _confusion_matrix(y_train, pred_train, n_classes)

        # Test on each operator
        for test_op in op_names:
            if test_op not in op_fields:
                continue
            X_test = op_fields[test_op].reshape(total_samples, -1)
            logits_test = X_test @ W
            pred_test = np.argmax(logits_test, axis=1)
            acc = float(np.mean(pred_test == y_train))
            cross_accs[test_op] = acc
            if test_op != train_op:
                ratios[f"{train_op}_vs_{test_op}"] = float(
                    same_acc / (acc + 1e-30)
                )
            if test_op == train_op:
                cm = _confusion_matrix(y_train, pred_test, n_classes)

        # Compute mechanism-specific rates
        # False via rate: fraction of H0 cases predicted as H1
        h0_mask = y_train == 0
        if np.sum(h0_mask) > 0:
            false_via = float(np.mean(pred_train[h0_mask] == 1))
        else:
            false_via = 0.0

        # Return-path confusion: fraction of H3 predicted as H1
        h3_mask = y_train == 3
        if np.sum(h3_mask) > 0:
            rp_conf = float(np.mean(pred_train[h3_mask] == 1))
        else:
            rp_conf = 0.0

        results[train_op] = MechanismDecisionResult(
            operator=train_op,
            same_operator_accuracy=same_acc,
            cross_operator_accuracy=cross_accs,
            cross_same_accuracy_ratio=ratios,
            confusion_matrix=cm.tolist() if hasattr(cm, 'tolist') else [[int(v) for v in row] for row in cm],
            false_via_rate=false_via,
            return_path_confusion_rate=rp_conf,
            n_samples_per_class=n_per_class,
        )

    return results


def _confusion_matrix(y_true: np.ndarray, y_pred: np.ndarray, n_classes: int) -> np.ndarray:
    cm = np.zeros((n_classes, n_classes), dtype=int)
    for t, p in zip(y_true, y_pred):
        cm[int(t), int(p)] += 1
    return cm


# --- Round 3: Template/spectral nearest-centroid mechanism scorer ---

@dataclass
class TemplateScorerResult:
    """Results from template evidence scorer for mechanism decision instability."""
    operator: str = ""
    scorer_name: str = "template_evidence"
    same_operator_accuracy: float = 0.0
    cross_operator_accuracy: Dict[str, float] = field(default_factory=dict)
    instability_ratio: Dict[str, float] = field(default_factory=dict)  # same / cross
    confusion_matrix: List[List[int]] = field(default_factory=list)
    false_via_rate: float = 0.0
    return_path_confusion_rate: float = 0.0
    n_samples_per_class: int = 0


class TemplateEvidenceScorer:
    """Dependency-free template evidence classifier.

    For each hypothesis class h, builds a template field pattern T_h from
    class-mean training fields. At test time, scores each test field y by:

        score_h(y) = min_alpha || y - alpha * T_h ||^2

    The optimal scalar is alpha* = (T_h^T y) / (T_h^T T_h), giving
    residual = ||y||^2 - (T_h^T y)^2 / (T_h^T T_h).

    Prediction = argmin_h residual_h(y).
    """

    def __init__(self):
        self.templates_: np.ndarray = None  # (n_classes, n_features)
        self.template_norms_sq_: np.ndarray = None  # (n_classes,)
        self.classes_: List[int] = []
        self.n_classes_: int = 0

    def fit(self, X: np.ndarray, y: np.ndarray) -> "TemplateEvidenceScorer":
        """Build class-mean templates from training data.

        Templates are raw class means (not normalized). Scoring uses the
        normalized residual: argmin_h ||y - alpha*T_h||^2 / ||y||^2.

        Args:
            X: (n_samples, n_features) field observations.
            y: (n_samples,) integer class labels.
        """
        self.classes_ = sorted(set(int(c) for c in y))
        self.n_classes_ = len(self.classes_)
        self.templates_ = np.zeros((self.n_classes_, X.shape[1]))
        for idx, c in enumerate(self.classes_):
            mask = y == c
            if np.sum(mask) > 0:
                self.templates_[idx] = X[mask].mean(axis=0)
        self.template_norms_sq_ = np.sum(self.templates_ ** 2, axis=1)
        return self

    def predict(self, X: np.ndarray) -> np.ndarray:
        """Predict by minimum-normalized template residual.

        residual_h = ||y - alpha*T_h||^2 / ||y||^2 = 1 - (T_h^T y)^2 / (||T_h||^2 * ||y||^2)

        Equivalent to argmax_h |cos(y, T_h)| (cosine similarity nearest-centroid).
        """
        dots = X @ self.templates_.T  # (n_samples, n_classes)
        x_norms_sq = np.sum(X ** 2, axis=1, keepdims=True)  # (n_samples, 1)
        eps = 1e-30
        # Cosine similarity squared, accounting for template norm
        cos_sq = (dots ** 2) / (self.template_norms_sq_[None, :] * x_norms_sq + eps)
        # For zero-norm test points, fall back to raw dot products
        zero_mask = (x_norms_sq < eps).ravel()
        if np.any(zero_mask):
            cos_sq[zero_mask] = np.abs(dots[zero_mask]) / (np.sqrt(self.template_norms_sq_[None, :]) + eps)
        pred_indices = np.argmax(cos_sq, axis=1)
        return np.array([self.classes_[i] for i in pred_indices])

    def predict_cross_operator(self, X: np.ndarray, op_templates: Dict[int, np.ndarray]) -> np.ndarray:
        """Predict using templates from a different operator via cosine similarity."""
        n_classes = len(op_templates)
        classes = sorted(op_templates.keys())
        templates = np.stack([op_templates[c] for c in classes], axis=0)
        tn_sq = np.sum(templates ** 2, axis=1)
        dots = X @ templates.T
        x_norms_sq = np.sum(X ** 2, axis=1, keepdims=True)
        eps = 1e-30
        cos_sq = (dots ** 2) / (tn_sq[None, :] * x_norms_sq + eps)
        zero_mask = (x_norms_sq < eps).ravel()
        if np.any(zero_mask):
            cos_sq[zero_mask] = np.abs(dots[zero_mask]) / (np.sqrt(tn_sq[None, :]) + eps)
        pred_indices = np.argmax(cos_sq, axis=1)
        return np.array([classes[i] for i in pred_indices])


def run_template_mechanism_stress(
    points: np.ndarray,
    geom_segments: List,
    op_names: List[str],
    n_per_class: int = 25,
    current_max_mA: float = 10.0,
    noise_std_uT: float = 0.1,
    rng: np.random.Generator = None,
) -> Dict[str, TemplateScorerResult]:
    """Run mechanism decision instability with template evidence scorer.

    For each operator:
    1. Generate mechanism-labeled currents (H0/H1/H2/H3)
    2. Compute fields using that operator
    3. Build class-mean templates
    4. Evaluate same-operator accuracy
    5. For each OTHER operator, evaluate cross-operator accuracy using the
       templates learned from the training operator
    6. Report confusion matrices, false-via, return-path confusion
    """
    if rng is None:
        rng = np.random.default_rng(0)

    n_components = min(len(geom_segments), 4)
    if n_components < 2:
        return {}

    # Generate mechanism-labeled currents
    currents_dict = {}
    labels_dict = {}
    for op_name in op_names:
        currents, labels = generate_mechanism_currents(
            n_per_class, n_components, current_max_mA, rng
        )
        currents_dict[op_name] = currents
        labels_dict[op_name] = labels

    # Generate fields for each operator
    op_fields = {}
    for op_name in op_names:
        op_fields[op_name] = compute_mechanism_fields(
            currents_dict[op_name], points, geom_segments, op_name,
            noise_std_uT=noise_std_uT, rng=rng,
        )

    results = {}

    for train_op in op_names:
        if train_op not in op_fields:
            continue

        X_train = op_fields[train_op].reshape(len(currents_dict[train_op]), -1)
        y_train = labels_dict[train_op]

        # Fit template scorer on this operator's fields
        scorer = TemplateEvidenceScorer()
        scorer.fit(X_train, y_train)

        # Same-operator evaluation
        pred_same = scorer.predict(X_train)
        same_acc = float(np.mean(pred_same == y_train))
        cm_same = _confusion_matrix(y_train, pred_same, scorer.n_classes_)

        # Cross-operator evaluation
        cross_accs = {}
        ratios = {}
        # Store per-class templates for cross-operator use
        op_templates = {}
        for idx, c in enumerate(scorer.classes_):
            mask = y_train == c
            if np.sum(mask) > 0:
                op_templates[int(c)] = X_train[mask].mean(axis=0)

        for test_op in op_names:
            if test_op not in op_fields:
                continue
            X_test = op_fields[test_op].reshape(len(currents_dict[test_op]), -1)
            y_test = labels_dict[test_op]

            if test_op == train_op:
                pred = scorer.predict(X_test)
            else:
                # Use templates from train_op, test on test_op's fields
                pred = scorer.predict_cross_operator(X_test, op_templates)

            acc = float(np.mean(pred == y_test))
            cross_accs[test_op] = acc
            if test_op != train_op:
                ratios[f"{train_op}_vs_{test_op}"] = float(
                    same_acc / (acc + 1e-30)
                )

        # Mechanism-specific rates
        h0_mask = y_train == 0
        false_via = float(np.mean(pred_same[h0_mask] == 1)) if np.sum(h0_mask) > 0 else 0.0

        h3_mask = y_train == 3
        rp_conf = float(np.mean(pred_same[h3_mask] == 1)) if np.sum(h3_mask) > 0 else 0.0

        results[train_op] = TemplateScorerResult(
            operator=train_op,
            scorer_name="template_evidence",
            same_operator_accuracy=same_acc,
            cross_operator_accuracy=cross_accs,
            instability_ratio=ratios,
            confusion_matrix=cm_same.tolist() if hasattr(cm_same, 'tolist') else [[int(v) for v in row] for row in cm_same],
            false_via_rate=false_via,
            return_path_confusion_rate=rp_conf,
            n_samples_per_class=n_per_class,
        )

    return results


