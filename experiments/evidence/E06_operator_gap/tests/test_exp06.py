import json
import importlib.util
from pathlib import Path
import sys

import numpy as np

ROOT = Path(__file__).resolve().parents[1]
SPEC = importlib.util.spec_from_file_location("exp06_run_all", ROOT / "src" / "run_all.py")
assert SPEC is not None and SPEC.loader is not None
RUN_ALL = importlib.util.module_from_spec(SPEC)
sys.modules[SPEC.name] = RUN_ALL
SPEC.loader.exec_module(RUN_ALL)

build_basis_high_surrogate = RUN_ALL.build_basis_high_surrogate
build_basis_low = RUN_ALL.build_basis_low
build_basis_medium_surrogate = RUN_ALL.build_basis_medium_surrogate
load_config = RUN_ALL.load_config
make_grid = RUN_ALL.make_grid
physics_decoder_from_low_basis = RUN_ALL.physics_decoder_from_low_basis
predict_physics = RUN_ALL.predict_physics
sample_currents = RUN_ALL.sample_currents
synthesize_fields = RUN_ALL.synthesize_fields
rel_l2 = RUN_ALL.rel_l2
matched_features = RUN_ALL.matched_features
fit_ridge = RUN_ALL.fit_ridge
apply_ridge = RUN_ALL.apply_ridge
load_real_pypeec_bridge = RUN_ALL.load_real_pypeec_bridge


def test_low_operator_physics_decoder_is_nearly_exact_without_noise():
    cfg = load_config()
    grid = make_grid(32, 1200.0, 0.0)
    basis_low, _ = build_basis_low(grid, cfg)
    rng = np.random.default_rng(123)
    X = sample_currents(rng, 50, 20.0)
    Y = synthesize_fields(X, basis_low, noise_std_T=0.0, rng=rng)
    decoder = physics_decoder_from_low_basis(basis_low)
    pred = predict_physics(Y, decoder)
    assert rel_l2(pred, X) < 1e-8


def test_high_surrogate_differs_from_low_operator():
    cfg = load_config()
    grid = make_grid(32, 1200.0, 0.0)
    basis_low, _ = build_basis_low(grid, cfg)
    basis_medium, _ = build_basis_medium_surrogate(grid, cfg)
    basis_high, _ = build_basis_high_surrogate(grid, cfg)
    medium_diff = np.linalg.norm(basis_medium - basis_low) / np.linalg.norm(basis_low)
    diff = np.linalg.norm(basis_high - basis_low) / np.linalg.norm(basis_low)
    assert 0 < medium_diff < diff
    assert diff > 0.05


def test_small_high_fidelity_calibration_improves_operator_mismatch():
    cfg = load_config()
    grid = make_grid(32, 1200.0, 0.0)
    basis_low, _ = build_basis_low(grid, cfg)
    basis_high, _ = build_basis_high_surrogate(grid, cfg)
    rng = np.random.default_rng(321)
    X_cal = sample_currents(rng, 200, 20.0)
    X_test = sample_currents(rng, 200, 20.0)
    Y_cal = synthesize_fields(X_cal, basis_high, noise_std_T=0.006e-6, rng=rng)
    Y_test = synthesize_fields(X_test, basis_high, noise_std_T=0.006e-6, rng=rng)
    decoder = physics_decoder_from_low_basis(basis_low)
    pred_before = predict_physics(Y_test, decoder)
    feat_cal = matched_features(Y_cal, basis_low)
    feat_test = matched_features(Y_test, basis_low)
    W, b = fit_ridge(feat_cal, X_cal, lam=1e-12)
    pred_after = apply_ridge(feat_test, W, b)
    assert rel_l2(pred_after, X_test) < rel_l2(pred_before, X_test)


